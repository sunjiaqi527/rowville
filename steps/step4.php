			<h1>Why do we need a <br>
			business plan?</h1>
			<div id="blocks1"><p>Your business plan not only helps to keep you on track but is extremely important when it comes to needing money for your business. If you want to get a business loan or attract an investor they will want to see your business plan to show that you have thought out all the aspects of your business and why it’s worth loaning you the money or investing in your business.</p></div>
			<div id="blocks2"><p>Creating a business plan is important and making sure it’s updated regularly is also important to ensure your business keeps moving forward in the right direction.</p></div>
			<div id="blocks3"><p>You can have a formal detailed business plan or you can have a less formal one page overview of your business plan. It depends on the type and size of the business you want to create and whether or not you will be applying for a loan or needing to find investors.</p></div>
