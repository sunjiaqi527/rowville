	<ol>
		<h2><ul>What is an ABN? </ul></h2>
	</ol>
	<p>An ABN is a unique number that identifies your business to the government and community.
	To start a business you must apply an ABN. Click the link to directly apply<a href="https://abr.gov.au/ABRWeb/AbnApply.abr?pid=71">here</a></p>
	<ol>
		<h2><ul>Benefit of an ABN </ul></h2>
	</ol>
	<ul>
		<li>With ABN you can register a “.com.au” website. Access to government information, services and concessions for business.</li>
		<li>You can establish a business identity when selling to customers and other businesses.</li>
		<li>You will gain the ability to claim tax deductions for business expenses against your taxable income. </li>
		<li>It will potentially provide you the flexibility to manage your time and work your own hours.</li>
	
	</ul>
	<ol>
		<h2><ul>Do you need to register a business name?</ul></h2>
	</ol>
	<p>If your business name is your, or your partner's, first name and surname, then there is no need to register.</p>
	<p>For example</p>
	<ul>
		<li>John Smith has a jewellery business named John Smith '. 
		As his business is trading under his own name, he does not need to register for a business name.</li>
		<li>Jane Smith has a floral business named 'Hart Flowers'. 
		As her business is not trading under her own name, she will need to register the business name 'Hart Flowers'.</li>
		

	</ul>
	<ol>
		<h2><ul>To register your business name:</ul></h2>
	</ol>
	<ol>
		<li>If you don’t have An ASIC Connect Account. Click the link to register An ASIC account 
		<a href="https://asicconnect.asic.gov.au/public/faces/signUp?_afrLoop=340415622662000&_afrWindowMode=0&_adf.ctrl-state=7j4uo9083_29">Register ASIC Account</a> </li>
		<p>If you do procced to 2</p> <br>
		<li>Go to ASIC Connect and log in to your account. 
		<a href="https://asicconnect.asic.gov.au/public/faces/landingPage?_afrLoop=398059874783000&amp;_afrWindowMode=0&amp;_adf.ctrl-state=154jnr50c_39&_ga=1.146592502.335097381.1441671572 ">ASIC Connect</a></li>
		<li>If you're logging in for the first time, make sure you say 'No', you don’t have a current business.  </li>
		<li>Select the Licences and Registrations tab at the top of the ASIC Connect page.</li>
		<li>Select you would like to register a business name from the drop-down box.</li>
		<li>Read the information and tick the boxes. Select 'Get started'.</li>
		<li>Select the Licences and Registrations tab at the top of the ASIC Connect page.</li>
		<li>Select the relevant ABN option, enter your ABN, and select 'Next'. <b> You must enter your ABN without any spaces.</b></li>
		<li>Check your business name availability. <a href="https://connectonline.asic.gov.au/RegistrySearch/faces/landing/bn/SearchBnRegisters.jspx?_adf.ctrl-state=1021to5wy0_4">Business name availability check Link</a></li>
		<li>Enter the proposed business name exactly as you would like it to appear on our register. Business names are case sensitive.</li>
		<li>Select the registration period.</li>
		<li>Enter the proposed business name holder details.</li>
		<li>Enter the addresses of the proposed business name.</li>
		<li>Confirm the eligibility to hold the proposed business name.</li>
		<li>Review your application. Make sure your business name is spelt correctly.</li>
		<li>Complete your declarations.</li>
		<li>Make your payment.</li>
		<li>If you don't pay within 10 days, your application will be cancelled and you will need to reapply.</li>
		<li>Confirmation.</li>
	</ol>
	
