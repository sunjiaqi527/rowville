	<ol><h2>Common Compulsory insurance</h2></ol>


<p>If you wish to start a business in Australian, you will be required to get the following insurances as a requirement of law.</p>
<ol><h2>Workers’ compensation</h2></ol>
<p>This is the insurance you must apply if you are hiring employees.
 More information on workers compensation insurance can be found <a href="https://www.business.gov.au/Risk-management/Insurance/Workers-compensation-insurance">here</a></p>
    <ol><h2>Public liability insurance</h2></ol>
	<p>Public liability insurance covers third party’s injury and death. Certain business must apply for public liability insurance.</p>
	<p>More information on Public liability insurance can be found <a href="https://www.business.gov.au/Risk-management/Insurance/Liability-insurance-and-professional-indemnity/#_Public_liability">here</a></p>
    <ol><h2>Third party personal injury insurance</h2></ol>
	<p>Third party personal injury insurance is usually covered when you purchase your motor vehicle. 
	It is often part of your vehicle registration fee.</p>
	<p>Declaration: Insurance policy should be discussed with the professional insurance company and further requirement may be applied vary to business type.
	 More information on various insurances applying to business can be found <a href="https://www.business.gov.au/risk-management/insurance">here</a></p>
