		<h2><li>Understanding Marketing </li></h2>
		<p>Every business needs marketing, be it big or small. Marketing should give a reason for customers to buy your product or service instead of your competitors. 
		Marketing should help to gain new customers and develop a business idea according to their needs</p>
		<p>Marketing covers the following:</p>
		<ol>
			<li>Your market position</li>
			<li>Your target audience</li>
			<li>Pricing of the product or services you offer</li>
			<li>Quality of the product or services you offer</li>
		</ol>
		
		<p>Hence having a marketing plan in place is very important to sustain in the market ad run a successful business.</p>
		<p>For more knowledge about marketing and its benefits<a href="https://www.business.gov.au/Planning/Marketing-plans"> visit</a> business Victoria website</p>
		
		<h2><li>Types of marketing  </li></h2>
		<p>Some common type of marketing are:</p>
		
		<h3>Social media marketing</h3>
		<p>Social media marketing can be a very effective tool for all size businesses. 
		They give you a channel to communicate with your customers and prospective customers in real time. 
		They can expand your networks, increase market reach as well. 
		Thousands of companies use free social media services like Facebook, Twitter, Pinterest, Instagram, vine to advertise their products. 
		A small amount of fee to get sponsored advertisement on these mediums can save a lot of money generally spent on advertisement. 
		More information about social media marketing can be found <a href="https://www.business.gov.au/Marketing/Social-media">here</a> </p>
		
		<h3>Email marketing</h3>
		<p>Email marketing is one of the easiest ways of promoting your product/service. 
		Find a reason for customers to provide you the email, for example, one-time coupon, subscription etc 
		and then using these emails to update customers about promotions, discounts, and exclusive offers is an effective way to market your idea.</p>
		
		<h3>SEO marketing</h3>
		<p>Search engine optimization is a way to increase your visibility on the internet. 
		One of the easiest field to break into and hardest to succeed in is the is Search Engine Optimization. 
		To stimulate traffic, SEO can be incorporated into the content of your blogs and websites. 
		It is easier to collect income being ranked the highest by the search engine.</p>
		
		<h3>Event Marketing</h3>
		<p>Scheduling events for customers like events on festivals where customers can get discounts on products or services can be another effective way to market your ideas and lure new customers. 
		Incorporating various offers, discounts, giveaways with the seasonal event can be very engaging and exciting for customers</p>
		
		<h2><li>Creating a market plan</li></h2>
		<p>Once you have done your research and created a business plan, the next step towards setting up your business is to have a marketing plan. 
		A market plan will require in-depth knowledge and research about your current market, your key strategies, proposed budget etc. 
		A marketing plan is an ongoing process and some of the strategies will have to change as the business changes. </p>
		<p>For example, initially, you might have entered the market as a cost leader selling the cheapest product and now you have developed a product for a niche category customer, your marketing plan and strategy will have to change. 
		The approach used as a cost leader may not work as a differentiator. </p>
		
